public class ConnectFour
{

    // Width and height of the board (Standard is 6x7)
    int boardx = 7; int boardy = 6;

    // Declaring the variable for the game board
    int[][] board;

    // Constructor Class
    public ConnectFour() {

        // Initializing the board
        board = new int[boardx][boardy];

    }



}
